package dao;

public class Member {
	private int ID;
	private String Name;
	private int Year;
	public void setID(int ID) {
		this.ID = ID;
	}
	public int getID() {
		return ID;
	}
	public void setName(String Name) {
		this.Name = Name;
	}
	public String getName() {
		return Name;
	}
	public void setYear(int Year) {
		this.Year = Year;
	}
	public int getYear() {
		return Year;
	}
}
